require_once '../db_connection.php';

function insertSectors($db, $sectors, $parent_id = null)
{
    foreach ($sectors as $sector) {
        $sector_id = $sector['value'];
        $sector_name = $sector['label'];

        $stmt = $db->prepare("INSERT INTO sectors (sector_id, sector_name, parent_sector_id) VALUES (:sector_id, :sector_name, :parent_sector_id)");
        $stmt->bindParam(':sector_id', $sector_id);
        $stmt->bindParam(':sector_name', $sector_name);
        $stmt->bindParam(':parent_sector_id', $parent_id);
        $stmt->execute();

        if (isset($sector['children'])) {
            insertSectors($db, $sector['children'], $sector_id);
        }
    }
}

